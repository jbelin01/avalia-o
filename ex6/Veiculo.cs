public class Veiculo
{

    public int velMax;

    public string modelo;

    public string marca;

    public int ano;

    public Veiculo(){

    }

    public Veiculo(int velMax, string modelo, string marca, int ano)
    {

        this.velMax = velMax;
        this.modelo = modelo;
        this.marca = marca;
        this.ano = ano;

    }


    


}